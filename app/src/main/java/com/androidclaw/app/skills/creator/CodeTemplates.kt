            false
        }
    }
"""

    /**
     * SharedPreferences 模板
     */
    fun sharedPreferencesTemplate(): String = """
    /**
     * 保存数据到 SharedPreferences
     */
    private fun savePreference(key: String, value: Any) {
        val context = this.context ?: return
        val prefs = context.getSharedPreferences("\${skillName}_prefs", Context.MODE_PRIVATE)
        
        prefs.edit().apply {
            when (value) {
                is String -> putString(key, value)
                is Int -> putInt(key, value)
                is Long -> putLong(key, value)
                is Boolean -> putBoolean(key, value)
                is Float -> putFloat(key, value)
            }
            apply()
        }
    }
    
    /**
     * 从 SharedPreferences 读取数据
     */
    private fun <T> getPreference(key: String, defaultValue: T): T {
        val context = this.context ?: return defaultValue
        val prefs = context.getSharedPreferences("\${skillName}_prefs", Context.MODE_PRIVATE)
        
        @Suppress("UNCHECKED_CAST")
        return when (defaultValue) {
            is String -> prefs.getString(key, defaultValue) as T
            is Int -> prefs.getInt(key, defaultValue) as T
            is Long -> prefs.getLong(key, defaultValue) as T
            is Boolean -> prefs.getBoolean(key, defaultValue) as T
            is Float -> prefs.getFloat(key, defaultValue) as T
            else -> defaultValue
        }
    }
"""

    /**
     * Intent 启动模板
     */
    fun intentTemplate(): String = """
    /**
     * 启动 Activity
     */
    private fun startActivity(action: String, dataUri: Uri? = null, extras: Map<String, Any>? = null) {
        val context = this.context ?: return
        
        val intent = Intent(action).apply {
            dataUri?.let { data = it }
            extras?.forEach { (key, value) ->
                when (value) {
                    is String -> putExtra(key, value)
                    is Int -> putExtra(key, value)
                    is Boolean -> putExtra(key, value)
                    is Long -> putExtra(key, value)
                }
            }
            addFlags(Intent.FLAG_ACTIVITY_NEW_TASK)
        }
        
        context.startActivity(intent)
    }
"""

    /**
     * 生成 skill_manifest.json
     */
    fun manifestTemplate(
        skillName: String,
        displayName: String,
        description: String,
        version: String = "1.0.0",
        author: String = "AndroidClaw User",
        permissions: List<String>,
        tools: List<ToolSpec>
    ): String {
        val manifestTools = tools.joinToString(",\n") { tool ->
            """
        {
          "name": "${tool.name}",
          "description": "${tool.description}",
          "parameters": [
            ${tool.parameters.joinToString(",\n            ") { param ->
                """{"name": "${param.name}", "type": "${param.type}", "required": ${param.required}, "description": "${param.description}"}"""
            }}
          ]
        }"""
        }

        return """
{
  "name": "$skillName",
  "version": "$version",
  "display_name": "$displayName",
  "description": "$description",
  "author": "$author",
  "license": "Apache-2.0",
  "min_androidclaw_version": "1.0.0",
  "permissions": [
    ${permissions.joinToString(",\n    ") { "\"$it\"" }}
  ],
  "entry_point": "${skillName}_skill.kt",
  "tools": [$manifestTools
  ],
  "assets": [],
  "dependencies": []
}
""".trimIndent()
    }


    /**
     * 基础 Skill 模板
     */
    fun baseSkillTemplate(
        skillName: String,
        displayName: String,
        description: String,
        permissions: List<String>,
        tools: List<ToolSpec>
    ): String {
        val permissionList = if (permissions.isNotEmpty()) {
            permissions.joinToString(",\n        ") { 
                val permName = it.substringAfterLast(".")
                "android.Manifest.permission.$permName"
            }
        } else {
            ""
        }
        
        val toolsList = tools.mapIndexed { index, tool ->
            """
        ToolDefinition(
            toolName = "${tool.name}",
            displayName = "${tool.description.take(20)}",
            description = "${tool.description}",
            parameters = listOf(
                ${tool.parameters.joinToString(",\n                ") { param ->
                    """ToolParameter("${param.name}", "${param.type}", ${param.required}, "${param.description}")"""
                }}
            ),
            returnType = "${tool.returnType}"
        )${if (index < tools.size - 1) "," else ""}
            """
        }.joinToString("\n")

        return """
// ${skillName.replace("_", " ").capitalize()}.kt
// ${description}
// Auto-generated by AndroidClaw Skill Creator

package com.androidclaw.app.skills

import android.content.Context
import android.util.Log
${generateImports(tools)}

/**
 * ${displayName}
 * ${description}
 */
class ${skillName.split("_").joinToString("") { it.capitalize() }}Skill : SkillDefinition {

    companion object {
        private const val TAG = "${skillName.capitalize()}Skill"
    }

    private var context: Context? = null

    override val skillName: String = "$skillName"
    override val displayName: String = "$displayName"
    override val description: String = "$description"
    override val requiredPermissions: List<String> = listOf(
        $permissionList
    )

    override suspend fun initialize(context: Context) {
        this.context = context
        Log.i(TAG, "${displayName}Skill initialized")
    }

    override fun getTools(): List<ToolDefinition> = listOf(
$toolsList
    )

    override suspend fun executeTool(toolName: String, parameters: Map<String, Any>): ToolResult {
        return try {
            when (toolName) {
                ${tools.joinToString("\n                ") { tool -> 
                    "\"${tool.name}\" -> execute${tool.name.capitalize()}(parameters)"
                }}
                else -> ToolResult.Error("Unknown tool: \$toolName")
            }
        } catch (e: Exception) {
            Log.e(TAG, "Error executing \$toolName", e)
            ToolResult.Error("执行失败: \${e.message}", e)
        }
    }

    ${tools.joinToString("\n\n    ") { tool -> generateToolMethod(tool) }}

    override fun release() {
        context = null
        Log.i(TAG, "${displayName}Skill released")
    }
}
""".trimIndent()
    }

    /**
     * 生成导入语句
     */
    private fun generateImports(tools: List<ToolSpec>): String {
        val imports = mutableSetOf<String>()
        
        // 检查是否需要网络
        if (tools.any { it.returnType == "map" || it.parameters.any { p -> p.type == "map" } }) {
            imports.add("import org.json.JSONObject")
            imports.add("import com.google.gson.Gson")
            imports.add("import com.google.gson.reflect.TypeToken")
        }
        
        return imports.joinToString("\n")
    }

    /**
     * 生成工具方法
     */
    private fun generateToolMethod(tool: ToolSpec): String {
        val params = tool.parameters.joinToString(", ") { param ->
            val paramRef = when (param.type) {
                "string" -> "params[\"${param.name}\"] as? String ?: \"\""
                "int" -> "(params[\"${param.name}\"] as? Number)?.toInt() ?: 0"
                "boolean" -> "params[\"${param.name}\"] as? Boolean ?: false"
                "float" -> "(params[\"${param.name}\"] as? Number)?.toFloat() ?: 0f"
                else -> "params[\"${param.name}\"]"
            }
            paramRef
        }
        
        return """
    private suspend fun execute${tool.name.capitalize()}(params: Map<String, Any>): ToolResult {
        Log.d(TAG, "Executing ${tool.name} with params: \$params")
        
        // TODO: 实现具体逻辑
        // 参数: ${tool.parameters.joinToString(", ") { "${it.name}: ${it.type}" }}
        
        return ToolResult.Success(mapOf(
            "status" to "success",
            "message" to "功能待实现"
        ))
    }
""".trimIndent()
    }

    /**
     * 网络请求模板 - GET
